int		ft_count_if(char **tab, int (*f)(char*))
{
	int		i;
	int		j;

	if (tab == 0 || f == 0)
		return (0);
	i = 0;
	j = 0;
	while (tab[i])
	{
		if (f(tab[i]))
		{
			j = 0;
			while (tab[i][j])
				j++;
			return (j);
		}
		i++;
	}
	return (0);
}
